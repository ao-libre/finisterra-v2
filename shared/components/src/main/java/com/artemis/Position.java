package com.artemis;

import annotations.Public;
import com.artemis.annotations.PooledWeaver;

@Public
@PooledWeaver
public class Position extends Component {
    private int x;
    private int y;

    public Position() {}

    public Position(int x, int y) {
        this.x = x;
        this.y = y;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }

    void setX(int x) {
        this.x = x;
    }

    void setY(int y) {
        this.y = y;
    }


}
